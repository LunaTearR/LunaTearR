def octagon_area(x):

    """
    หาพื้นที่รูปแปดเหลียม 
    จากการแบ่งเป็นสี่เหลียมเท่าๆกัน 
    แล้วนำหาพื้นที่ของสี่เหลี่ยมแต่ละรูป
    จากนั้นนำมาคูณกันทั้งหมด
    
    """
    return ((x/3)**2)*7

def main():
    # รับด้านจาก user
    x = int(input())
    # พิมพ์พื้นที่ของรูปแปดเหลี่ยมด้านเท่า
    print(octagon_area(x))

if __name__ == '__main__':
    main()
